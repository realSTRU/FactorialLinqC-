using System;
using System.Collections.Generic;
using System.Linq;

namespace Program
{
    class Program
    {
        static void Main(){

            int val;
            List<int> valor = new List<int>();

            Console.WriteLine("Digiete el numero para calcular el factorial:");
            val = Convert.ToInt32(Console.ReadLine());

            Console.Clear();

            //Añadiendo a la lista para calcular el factorial del numero ingresado.//
            for(int i = val;i>0;i--){
                valor.Add(i);
            }

            Console.WriteLine("la secuencia factorial a procesar es la siguiente:");
            //se imprime la secuencia de numero a procesar en formato factorial ((nx)x(nx-1)x(nx-2)......)
            printSecuencia(valor);
            

            Console.ReadKey();
            Console.Clear();

            //impresion de la respuesta//
            int RES= factorialLinq(valor);
            Console.WriteLine($"El factorial del numero {val} es: {RES}");




        



        }


   
        static int factorialLinq(List<int> almacen){

            IEnumerable<int> AlmacenLinq = from i in almacen select i;
            int OP = AlmacenLinq.Aggregate((anterior,siguiente) => anterior * siguiente);

            return OP; 


        }

        static void printSecuencia(List<int> valor){

            foreach(int i in valor){
                if(i != 1){
                  Console.Write($"{i}x");  
                }
                else{
                    Console.Write(i);

                }
            }
        }
    
    }
}

